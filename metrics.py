import pandas as pd

def transform_interceptions_metrics(passes_csv, save=True, output_name="interceptions_metrics_fixed.csv"):
    df = pd.read_csv(passes_csv)
    df.sort_values(by='frame_num', inplace=True)
    df.reset_index(drop=True, inplace=True)

    interceptions = []

    for i, row in df.iterrows():
        team = row['team_id_1']
        success = row['success']
        fail = row['fail']
        frame = row['frame_num']
        position = (row['position_x'], row['position_y'])

        # If this is a failed pass, track it as a ball loss
        if fail == 1 and success == 0:
            # Row 1: Player who lost the ball
            interceptions.append({
                'shirt_number': row['shirt_num1'],
                'team_id': team,
                'ball_loss': 1,
                'ball_recovery': 0,
                'position': position,
                'start_frame_num': max(0,frame - 150),
                'end_frame_num': max(0,frame + 150)
            })

            # Row 2: Find the first pass from the other team = interception
            next_by_opponent = df[(df['frame_num'] > frame) & (df['team_id_1'] != team)]
            if not next_by_opponent.empty:
                interceptor = next_by_opponent.iloc[0]
                interceptions.append({
                    'shirt_number': interceptor['shirt_num1'],
                    'team_id': interceptor['team_id_1'],
                    'ball_loss': 0,
                    'ball_recovery': 1,
                    'position': position,  # 👈 same position as the ball loss
                    'start_frame_num': max(0,interceptor['frame_num'] - 150),
                    'end_frame_num': max(0,interceptor['frame_num'] + 150)
                })

    df_interceptions = pd.DataFrame(interceptions)

    if save:
        df_interceptions.to_csv(output_name, index=False)

    return df_interceptions
