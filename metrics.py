import pandas as pd

def transform_shots_metrics(shots_csv, passes_csv, save=True, output_name="shots_metrics_fixed.csv"):
    shots_df = pd.read_csv(shots_csv)
    passes_df = pd.read_csv(passes_csv)

    shots_df['position'] = list(zip(shots_df['position_x'], shots_df['position_y']))
    shots_df['frame_num'] = shots_df['frame_nur']

    passes_df.sort_values(by='frame_num', inplace=True)
    passes_df.reset_index(drop=True, inplace=True)

    output_rows = []

    for _, shot in shots_df.iterrows():
        shot_frame = shot['frame_num']
        shot_team = shot['team_id_1']

        # --- ROW 1: Shooter
        output_rows.append({
            'shirt_number': shot['shirt_num1'],
            'team_id': shot['team_id_1'],
            'Key_passes': 0,
            'Shots_on_Target': shot['On Target'],
            'Shots_off_Target': shot['Off Target'],
            'Saved_shots': shot['Saved'],
            'Blocked_shots': shot['Blocked'],
            'Clearances': 0,
            'saves': 0,
            'penalties': shot['Penalty'],
            'position': (shot['position_x'], shot['position_y']),
            'start_frame_num': max(0,shot_frame - 150),
            'end_frame_num': max(0,shot_frame + 150)
        })

        # --- ROW 2: Key Passer
        key_pass = passes_df[
            (passes_df['frame_num'] < shot_frame) &
            (passes_df['team_id_1'] == shot_team) &
            (passes_df['success'] == 1)
        ]
        if not key_pass.empty:
            last_pass = key_pass.iloc[-1]
            output_rows.append({
                'shirt_number': last_pass['shirt_num1'],
                'team_id': last_pass['team_id_1'],
                'Key_passes': 1,
                'Shots_on_Target': 0,
                'Shots_off_Target': 0,
                'Saved_shots': 0,
                'Blocked_shots': 0,
                'Clearances': 0,
                'saves': 0,
                'penalties': 0,
                'position': (last_pass['position_x'], last_pass['position_y']),
                'start_frame_num': max(0, last_pass['frame_num'] - 150),
                'end_frame_num': max(0, last_pass['frame_num'] + 150)
            })

        # --- ROW 3: Clearance or Save (optional)
        label = None
        if shot['Blocked'] == 1 and shot['Saved'] == 0:
            label = 'Clearances'
        elif shot['Saved'] == 1 and shot['Blocked'] == 0:
            label = 'saves'

        if label:
            next_pass = passes_df[passes_df['frame_num'] > shot_frame]
            if not next_pass.empty:
                first_after = next_pass.iloc[0]
                output_rows.append({
                    'shirt_number': first_after['shirt_num1'],
                    'team_id': first_after['team_id_1'],
                    'Key_passes': 0,
                    'Shots_on_Target': 0,
                    'Shots_off_Target': 0,
                    'Saved_shots': 0,
                    'Blocked_shots': 0,
                    'Clearances': 1 if label == 'Clearances' else 0,
                    'saves': 1 if label == 'saves' else 0,
                    'penalties': 0,
                    'position': (first_after['position_x'], first_after['position_y']),
                    'start_frame_num': max(0,first_after['frame_num'] - 150),
                    'end_frame_num': max(0,first_after['frame_num'] + 150)
                })

    result_df = pd.DataFrame(output_rows)

    if save:
        result_df.to_csv(output_name, index=False)

    return result_df

