import pandas as pd

def transform_passing_network_metrics(passes_csv, save=True, output_name="passing_network_metrics.csv"):
    df = pd.read_csv(passes_csv)
    df.sort_values(by='frame_num', inplace=True)

    network_data = []
    current_chain = []
    current_shirts = []
    current_team = None
    current_type = None  # 'success' or 'fail'

    for _, row in df.iterrows():
        team = row['team_id_1']
        success = row['success']
        fail = row['fail']
        frame = row['frame_num']
        shirt = row['shirt_num1']

        # Identify pass type
        if success == 1 and fail == 0:
            pass_type = 'success'
        elif success == 0 and fail == 1:
            pass_type = 'fail'
        else:
            pass_type = None

        if pass_type:
            if current_team is None:
                current_team = team
                current_type = pass_type
                current_chain = [frame]
                current_shirts = [shirt]
            elif team == current_team and pass_type == current_type:
                current_chain.append(frame)
                current_shirts.append(shirt)
            else:
                # Save current chain before resetting
                network_data.append({
                    'team_id': current_team,
                    'pass_type': current_type,
                    'number_of_passes': len(current_chain),
                    'start_frame_num': current_chain[0] - 150,
                    'end_frame_num': current_chain[-1] + 150,
                    'shirt_numbers': [int(s) for s in current_shirts]
                })
                # Start new chain
                current_team = team
                current_type = pass_type
                current_chain = [frame]
                current_shirts = [shirt]
        else:
            # Save invalid current chain before skipping
            if len(current_chain) > 0:
                network_data.append({
                    'team_id': current_team,
                    'pass_type': current_type,
                    'number_of_passes': len(current_chain),
                    'start_frame_num': current_chain[0] - 150,
                    'end_frame_num': current_chain[-1] + 150,
                    'shirt_numbers': [int(s) for s in current_shirts]
                })
                current_team = None
                current_type = None
                current_chain = []
                current_shirts = []

    # Save last chain if it exists
    if len(current_chain) > 0:
        network_data.append({
            'team_id': current_team,
            'pass_type': current_type,
            'number_of_passes': len(current_chain),
            'start_frame_num': current_chain[0] - 150,
            'end_frame_num': current_chain[-1] + 150,
            'shirt_numbers': [int(s) for s in current_shirts]
        })

    network_df = pd.DataFrame(network_data)

    if save:
        network_df.to_csv(output_name, index=False)

    return network_df

