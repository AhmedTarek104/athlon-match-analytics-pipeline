def transform_passing_network_metrics(passes_csv, save=True, output_name="passing_network_metrics_fixed.csv"):
    df = pd.read_csv(passes_csv)
    df.sort_values(by='frame_num', inplace=True)

    network_data = []
    current_chain = []
    current_team = None
    current_type = None  # 'success' or 'fail'

    for _, row in df.iterrows():
        team = row['team_id_1']
        success = row['success']
        fail = row['fail']
        frame = row['frame_num']
        shirt_number = row['shirt_num1']
        pos_x = row['position_x']
        pos_y = row['position_y']

        # Determine pass type
        if success == 1 and fail == 0:
            pass_type = 'success'
        elif success == 0 and fail == 1:
            pass_type = 'fail'
        else:
            pass_type = None

        if pass_type:
            if current_team is None:
                current_team = team
                current_type = pass_type
                current_chain = [{
                    'frame': frame,
                    'shirt_number': shirt_number,
                    'position_x': pos_x,
                    'position_y': pos_y
                }]
            elif team == current_team and pass_type == current_type:
                current_chain.append({
                    'frame': frame,
                    'shirt_number': shirt_number,
                    'position_x': pos_x,
                    'position_y': pos_y
                })
            else:
                # Save previous chain
                if len(current_chain) > 0:
                    frames = [entry['frame'] for entry in current_chain]
                    shirt_numbers = [int(entry['shirt_number']) for entry in current_chain]
                    positions_x = [entry['position_x'] for entry in current_chain]
                    positions_y = [entry['position_y'] for entry in current_chain]

                    network_data.append({
                        'team_id': current_team,
                        'pass_type': current_type,
                        'number_of_passes': len(current_chain),
                        'start_frame_num': max(0, min(frames) - 150),
                        'end_frame_num': max(0, max(frames) + 150),
                        'position_x': np.mean(positions_x),
                        'position_y': np.mean(positions_y),
                        'shirt_number': shirt_numbers
                    })
                # Start new chain
                current_team = team
                current_type = pass_type
                current_chain = [{
                    'frame': frame,
                    'shirt_number': shirt_number,
                    'position_x': pos_x,
                    'position_y': pos_y
                }]
        else:
            # Save current chain if interrupted
            if len(current_chain) > 0:
                frames = [entry['frame'] for entry in current_chain]
                shirt_numbers = [int(entry['shirt_number']) for entry in current_chain]
                positions_x = [entry['position_x'] for entry in current_chain]
                positions_y = [entry['position_y'] for entry in current_chain]

                network_data.append({
                    'team_id': current_team,
                    'pass_type': current_type,
                    'number_of_passes': len(current_chain),
                    'start_frame_num': max(0, min(frames) - 150),
                    'end_frame_num': max(0, max(frames) + 150),
                    'position_x': np.mean(positions_x),
                    'position_y': np.mean(positions_y),
                    'shirt_number': shirt_numbers
                })
            # Reset chain
            current_team = None
            current_type = None
            current_chain = []

    # Final chain
    if len(current_chain) > 0:
        frames = [entry['frame'] for entry in current_chain]
        shirt_numbers = [int(entry['shirt_number']) for entry in current_chain]
        positions_x = [entry['position_x'] for entry in current_chain]
        positions_y = [entry['position_y'] for entry in current_chain]

        network_data.append({
            'team_id': current_team,
            'pass_type': current_type,
            'number_of_passes': len(current_chain),
            'start_frame_num': max(0, min(frames) - 150),
            'end_frame_num': max(0, max(frames) + 150),
            'position_x': np.mean(positions_x),
            'position_y': np.mean(positions_y),
            'shirt_number': shirt_numbers
        })

    # Output DataFrame
    network_df = pd.DataFrame(network_data)

    if save:
        network_df.to_csv(output_name, index=False)

    return network_df
