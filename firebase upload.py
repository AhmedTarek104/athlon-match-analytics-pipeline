def upload_match_summary(
    match_id,
    first_team_id,
    second_team_id,
    passes_csv,
    set_pieces_csv,
    shots_csv,
    tackles_csv,
    goals_csv
):
    # === Load CSVs
    df_passes = pd.read_csv(passes_csv)
    df_set = pd.read_csv(set_pieces_csv)
    df_shots = pd.read_csv(shots_csv)
    df_tackles = pd.read_csv(tackles_csv)
    df_goals = pd.read_csv(goals_csv)

    def get_team_name(val): return first_team_id if val in [1, '1'] else second_team_id

    # === ✅ Passes & Possession: only successful passes
    df_passes = df_passes[(df_passes['success'] == 1) & (df_passes['fail'] == 0)]
    df_passes['team'] = df_passes['team_id_1'].apply(get_team_name)
    total_passes = len(df_passes)
    passes = df_passes['team'].value_counts().to_dict()
    ball_possession = {
        first_team_id: round((passes.get(first_team_id, 0) / total_passes) * 100) if total_passes else 0,
        second_team_id: round((passes.get(second_team_id, 0) / total_passes) * 100) if total_passes else 0
    }

    # === Shirt Numbers (use all passes)
    df_all_passes = pd.read_csv(passes_csv)
    df_all_passes['team'] = df_all_passes['team_id_1'].apply(get_team_name)
    shirt_numbers = {
        first_team_id: sorted(df_all_passes[df_all_passes['team'] == first_team_id]['shirt_num1'].dropna().astype(int).unique().tolist()),
        second_team_id: sorted(df_all_passes[df_all_passes['team'] == second_team_id]['shirt_num1'].dropna().astype(int).unique().tolist())
    }

    # === Corners & Free Kicks
    df_set['team'] = df_set['team_id_1'].apply(get_team_name)
    corners = df_set[df_set['corner'] == 1]['team'].value_counts().to_dict()
    free_kicks = df_set[(df_set['direct kick'] == 1) | (df_set['indirect kick'] == 1)]['team'].value_counts().to_dict()

    # === ✅ Shots: dynamic team mapping
    shot_teams = df_shots['team_id'].unique()
    if len(shot_teams) == 2:
        shot_map = {shot_teams[0]: first_team_id, shot_teams[1]: second_team_id}
    else:
        shot_map = {shot_teams[0]: first_team_id}  # fallback

    df_shots['team'] = df_shots['team_id'].map(shot_map)
    shots_on_target = df_shots[df_shots['Shots_on_Target'] == 1]['team'].value_counts().to_dict()
    shots_saved = df_shots[df_shots['Saved_shots'] == 1]['team'].value_counts().to_dict()
    total_shots = df_shots['team'].value_counts().to_dict()

    # === ✅ Goals: dynamic team mapping
    goal_teams = df_goals['team_id'].unique()
    if len(goal_teams) == 2:
        goal_map = {goal_teams[0]: first_team_id, goal_teams[1]: second_team_id}
    else:
        goal_map = {goal_teams[0]: first_team_id}  # fallback

    df_goals['team'] = df_goals['team_id'].map(goal_map)
    total_goals = df_goals['team'].value_counts().to_dict()

    # === Tackles
    df_tackles['team'] = df_tackles['team_id'].apply(lambda x: first_team_id if x == 1 else second_team_id)
    tackles = df_tackles['team'].value_counts().to_dict()

    # === Helper: fallback to 0
    def safe(stat, team): return stat.get(team, 0)

    # === Final Data Structure
    match_data = {
        "team1_id": first_team_id,
        "team2_id": second_team_id,
        "ball_possession": {
            first_team_id: safe(ball_possession, first_team_id),
            second_team_id: safe(ball_possession, second_team_id)
        },
        "corners": {
            first_team_id: safe(corners, first_team_id),
            second_team_id: safe(corners, second_team_id)
        },
        "free_kicks": {
            first_team_id: safe(free_kicks, first_team_id),
            second_team_id: safe(free_kicks, second_team_id)
        },
        "passes": {
            first_team_id: safe(passes, first_team_id),
            second_team_id: safe(passes, second_team_id)
        },
        "shirt_numbers": shirt_numbers,
        "shots_on_target": {
            first_team_id: safe(shots_on_target, first_team_id),
            second_team_id: safe(shots_on_target, second_team_id)
        },
        "shots_saved": {
            first_team_id: safe(shots_saved, first_team_id),
            second_team_id: safe(shots_saved, second_team_id)
        },
        "tackles": {
            first_team_id: safe(tackles, first_team_id),
            second_team_id: safe(tackles, second_team_id)
        },
        "total_goals": {
            first_team_id: safe(total_goals, first_team_id),
            second_team_id: safe(total_goals, second_team_id)
        },
        "total_shots": {
            first_team_id: safe(total_shots, first_team_id),
            second_team_id: safe(total_shots, second_team_id)
        }
    }

    # === Upload to Firebase
    db.collection("matches").document(match_id).set(match_data, merge=True)
    print("✅ Match summary uploaded successfully.")


