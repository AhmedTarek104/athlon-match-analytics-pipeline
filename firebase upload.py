def upload_all_metrics(
    passes_csv, aerial_csv, ground_csv, interceptions_csv, tackles_csv, dribble_csv,
    set_pieces_csv, goals_csv, shots_csv, pass_network_csv,
    match_id,
    first_team_id="team1",
    second_team_id="team2"
):
    # === PASSES ===
    df_passes = pd.read_csv(passes_csv)
    df_passes[['position_x', 'position_y']] = pd.DataFrame(df_passes['position'].apply(eval).tolist(), index=df_passes.index)
    team_id_field = "team_id_1" if "team_id_1" in df_passes.columns else "team_id"

    passes_dict = {}
    for idx, row in df_passes.iterrows():
        team = first_team_id if row[team_id_field] == 1 else second_team_id
        passes_dict[str(idx)] = {
            "start_timestamp": int(row['start_frame_num']),
            "end_timestamp": int(row['end_frame_num']),
            "position_x": float(row['position_x']),
            "position_y": float(row['position_y']),
            "shirt_number": int(row['shirt_num1']),
            "success": row['success'] == 1 and row['fail'] == 0,
            "team_id": team
        }

    # === DUEL HELPER ===
    def process_duel(csv_path, success_col, fail_col, team_col_guess, shirt_col_guess, drop_condition=None):
        df = pd.read_csv(csv_path)
        df[['position_x', 'position_y']] = pd.DataFrame(df['position'].apply(eval).tolist(), index=df.index)
        team_col = team_col_guess if team_col_guess in df.columns else "team_id"
        shirt_col = shirt_col_guess if shirt_col_guess in df.columns else "shirt_number"
        if drop_condition:
            df = df[~drop_condition(df)]
        duel_dict = {}
        for idx, row in df.iterrows():
            team = first_team_id if row[team_col] == 1 else second_team_id
            success = row[success_col] == 1 and row[fail_col] == 0
            duel_dict[str(idx)] = {
                "start_timestamp": int(row['start_frame_num']),
                "end_timestamp": int(row['end_frame_num']),
                "position_x": float(row['position_x']),
                "position_y": float(row['position_y']),
                "shirt_number": int(row[shirt_col]),
                "success": success,
                "team_id": team
            }
        return duel_dict

    # === DUELS ===
    duels = {
        "aerial": process_duel(aerial_csv, "aerial_success", "aerial_failure", "team_id", "shirt_number"),
        "ground": process_duel(ground_csv, "ground_success", "ground_failure", "team_id", "shirt_number"),
        "interceptions": process_duel(interceptions_csv, "ball_recovery", "ball_loss", "team_id", "shirt_number"),
        "tackles": process_duel(tackles_csv, "tackling_success", "tackling_failure", "team_id", "shirt_number"),
        "dribbles": process_duel(dribble_csv, "dribble_success", "dribble_failure", "team_id_1", "shirt_number_1", drop_condition=lambda df: df["dribbled_past"] == 1)
    }

    # === SET PIECES ===
    df_set = pd.read_csv(set_pieces_csv)
    df_set[['position_x', 'position_y']] = pd.DataFrame(df_set['position'].apply(eval).tolist(), index=df_set.index)
    set_pieces_dict = {}
    for idx, row in df_set.iterrows():
        team = first_team_id if row["team_id_1"] == 1 else second_team_id
        if row['corner'] == 1: typ = 1
        elif row['throw'] == 1: typ = 2
        elif row['direct kick'] == 1: typ = 3
        elif row['indirect kick'] == 1: typ = 4
        elif row['penalty'] == 1: typ = 5
        else: continue
        set_pieces_dict[str(idx)] = {
            "start_timestamp": int(row['start_frame_num']),
            "end_timestamp": int(row['end_frame_num']),
            "position_x": float(row['position_x']),
            "position_y": float(row['position_y']),
            "shirt_number": int(row['shirt_num1']),
            "team_id": team,
            "type": typ
        }

    # === GOALS ===
    df_goals = pd.read_csv(goals_csv)
    if "position" not in df_goals.columns:
        df_goals["position"] = list(zip(df_goals["position_x"], df_goals["position_y"]))
    goals_dict = {}
    for idx, row in df_goals.iterrows():
        team = first_team_id if row["team_id"] == 0 else second_team_id
        goals_dict[str(idx)] = {
            "start_timestamp": max(0, int(row["start_frame_num"])),
            "end_timestamp": max(0, int(row["end_frame_num"])),
            "position_x": float(eval(row["position"])[0]),
            "position_y": float(eval(row["position"])[1]),
            "shirt_number": int(row["shirt_number"]),
            "type": 1 if row["Goal"] == 1 and row["Assist"] == 0 else 2,
            "team_id": team
        }

    # === SHOTS ===
    df_shots = pd.read_csv(shots_csv)
    if "position" not in df_shots.columns:
        df_shots["position"] = list(zip(df_shots["position_x"], df_shots["position_y"]))
    shots_dict = {}
    for idx, row in df_shots.iterrows():
        team = first_team_id if row["team_id"] == 0 else second_team_id
        shots_dict[str(idx)] = {
            "start_timestamp": max(0, int(row["start_frame_num"])),
            "end_timestamp": max(0, int(row["end_frame_num"])),
            "position_x": float(eval(row["position"])[0]),
            "position_y": float(eval(row["position"])[1]),
            "shirt_number": int(row["shirt_number"]),
            "type": 2 if row["Key_passes"] == 1 else 1,
            "success": row["Shots_on_Target"] == 1 and row["Shots_off_Target"] == 0,
            "team_id": team
        }

    # === PASS NETWORK ===
    df_network = pd.read_csv(pass_network_csv)
    network_dict = {}
    for idx, row in df_network.iterrows():
        shirts_raw = row["shirt_number"]
        shirts = eval(shirts_raw) if isinstance(shirts_raw, str) else shirts_raw
        pass_string = [f"{i} {int(shirts[i])}" for i in range(len(shirts))]

        network_dict[str(idx)] = {
            "start_timestamp": max(0, int(row["start_frame_num"])),
            "end_timestamp": max(0, int(row["end_frame_num"])),
            "pass_count": int(row["number_of_passes"]),
            "position_x": float(row["position_x"]),
            "position_y": float(row["position_y"]),
            "pass_string": pass_string
        }

    # === UPLOAD TO FIRESTORE ===
    match_ref = db.collection("match_analytics").document(match_id)
    match_ref.set({
        "distribution": {
            "passes": passes_dict,
            "pass_network": network_dict
        },
        "duels": duels,
        "set_pieces": set_pieces_dict,
        "goals": goals_dict,
        "shots": shots_dict
    }, merge=True)

    print("✅ All metrics uploaded successfully .")
